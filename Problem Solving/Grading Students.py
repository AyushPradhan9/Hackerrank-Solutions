def gradingStudents(grades):
    ogrades=[]
    for grade in grades:
        if grade<38:
            ogrades.append(grade)
        else:
            rem=grade%5
            if rem>=3:
                grade=grade+(5-rem)
            ogrades.append(grade)
    return ogrades


